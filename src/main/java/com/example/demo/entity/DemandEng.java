package com.example.demo.entity;


public class DemandEng {

  private String region;
  private String industry;
  private String description;
  private String stage;
  private String techCategories;
  private String tech;
  private String techOverview;
  private String source;
  private String number;
  private long demandId;


  public String getRegion() {
    return region;
  }

  public void setRegion(String region) {
    this.region = region;
  }


  public String getIndustry() {
    return industry;
  }

  public void setIndustry(String industry) {
    this.industry = industry;
  }


  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }


  public String getStage() {
    return stage;
  }

  public void setStage(String stage) {
    this.stage = stage;
  }


  public String getTechCategories() {
    return techCategories;
  }

  public void setTechCategories(String techCategories) {
    this.techCategories = techCategories;
  }


  public String getTech() {
    return tech;
  }

  public void setTech(String tech) {
    this.tech = tech;
  }


  public String getTechOverview() {
    return techOverview;
  }

  public void setTechOverview(String techOverview) {
    this.techOverview = techOverview;
  }


  public String getSource() {
    return source;
  }

  public void setSource(String source) {
    this.source = source;
  }


  public String getNumber() {
    return number;
  }

  public void setNumber(String number) {
    this.number = number;
  }


  public long getDemandId() {
    return demandId;
  }

  public void setDemandId(long demandId) {
    this.demandId = demandId;
  }

}
